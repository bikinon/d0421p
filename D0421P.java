    private void BtnBreezeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBreezeActionPerformed
    // Take input sizes and find true ID & OD
    double l = Double.parseDouble(lsz.getText());
    double w = Double.parseDouble(wsz.getText());
    double d = Double.parseDouble(dsz.getText());
    double breezeReelWdith = 1000;
    // Get Allowances
    draw0421pcls oDxf = new draw0421pcls();
    oDxf.length = 0;
    oDxf.width = 0;
    oDxf.depth = 0;
    oDxf.flute = (String) cflutetype.getSelectedItem();
    oDxf.allowanceSetup();
    
    getSizes();
    double ol = 25; // Paper Overlap
    double glOnBase = 25; // Insert glue section on front depth
    double btmPnlCutback = 15;
    double pD = this.idDepth + oDxf.dmain;
    double tmp = glOnBase + this.idWidth + oDxf.winner + ((pD) * 2) + ol; // Total Paper Length
    double paperW = this.idLength - 15; //8;
    double paperLbase = glOnBase + this.idWidth + oDxf.winner - btmPnlCutback; // Optimum size
    double paperLlid = tmp - paperLbase;   
    
    if((paperLbase + paperLlid) > breezeReelWdith) {
      JOptionPane.showMessageDialog(null, "If both sizes added together > 1000 then 2 passes\nmay be required to apply Breeze Paper\n" + String.format("A + B =%10.1f", (paperLbase + paperLlid)), "Breeze Paper Problem", JOptionPane.ERROR_MESSAGE);
    } // end of if
    if((paperLbase > breezeReelWdith) || (paperLlid > breezeReelWdith)) {
      JOptionPane.showMessageDialog(null, "Largest Paper size > 1000 then too big to make on Kohmann\n", "Breeze Paper ERROR", JOptionPane.ERROR_MESSAGE);  
    } // end of if 
    if((paperW > 400)) {
      JOptionPane.showMessageDialog(null, "Paper Width > 400 Possibly too big to make on Kohmann CHECK\n", "Breeze Paper ERROR", JOptionPane.ERROR_MESSAGE);  
    } // end of if 
    
    blksz.setText("BREEZE PAPER - Base :" + paperW + " x " + paperLbase + "   / Lid :" + paperW + " x " + paperLlid);

    }//GEN-LAST:event_BtnBreezeActionPerformed

    
      public void getSizes() {
    // Take input sizes and find true ID & OD
    double l = Double.parseDouble(lsz.getText());
    double w = Double.parseDouble(wsz.getText());
    double d = Double.parseDouble(dsz.getText());
    double lengthPlus = 0; // Bulge Figures
    double widthPlus = 0;
    double depthPlus = 0;
    // Get Allowances
    draw0421pcls oDxf = new draw0421pcls();
    oDxf.length = 0;
    oDxf.width = 0;
    oDxf.depth = 0;
    oDxf.flute = (String) cflutetype.getSelectedItem();
    oDxf.allowanceSetup();
    
    if (dimtype.getSelectedItem() == "ID: Internal Dimensions") {
      this.idLength = l; 
      this.idWidth = w;
      this.idDepth = d;   
    } else if (dimtype.getSelectedItem() == "OD: Outside Dimensions") {      
      if (cflutetype.getSelectedItem().toString() == "B") {   // put Bulge figres here later if necessary
      } else if(cflutetype.getSelectedItem().toString() == "C") {
      } else if(cflutetype.getSelectedItem().toString() == "BC") {  
      } else { // E flute  
      } // end if  
      this.idLength = l - oDxf.dataArray[4] - oDxf.odAlw - lengthPlus; // Assumes TOE section is Size given 
      this.idWidth = w - oDxf.dataArray[6] - oDxf.odAlw - widthPlus;  // assumes outer Width specified
      this.idDepth = d - oDxf.dataArray[0] - oDxf.odAlw - depthPlus;
      
    } else { // Bend to Bend
      this.idLength = l - oDxf.dataArray[4]; // Size - Allowance
      this.idWidth = w - oDxf.dataArray[5]; // assumes inner Width specified
      this.idDepth = d - oDxf.dataArray[0];
    } 
    // Set Front Flap & LidTab
    frontFlap = this.idDepth + oDxf.dataArray[2];
    lidTab = this.idDepth; // Max possible size - Amended in dsz_FocusLost
    // Find O/D
    this.odLength = this.idLength + oDxf.dataArray[4] + oDxf.odAlw + lengthPlus; 
    this.odWidth = this.idWidth + oDxf.dataArray[6] + oDxf.odAlw + widthPlus;
    this.odDepth = this.idDepth + oDxf.dataArray[0] + oDxf.odAlw + depthPlus;
    
    parcelSizes oBjt = new parcelSizes();
    oBjt.odLength = this.odLength;
    oBjt.odWidth = this.odWidth;
    oBjt.odDepth = this.odDepth;
    oBjt.checkSizes();
    
  } // getSizes

      
