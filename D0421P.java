    private void createDxfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createDxfActionPerformed
   getSizes();
    
    String outputDxfData = "";   
    
    // **** WARNINGS ****
    makeUpCheck oChk = new makeUpCheck();
    oChk.w = Double.parseDouble(wsz.getText());
    oChk.d = Double.parseDouble(dsz.getText());
    oChk.lidTab = this.lidTab;
    oChk.d0421pMakeUpCheck();
    // ************************************
    
    draw0421pcls oDxf = new draw0421pcls();
    oDxf.allowanceSetup();
    
    oDxf.xabs = 0;
    oDxf.yabs = 0;
    oDxf.length = this.idLength;
    oDxf.width = this.idWidth;
    oDxf.depth = this.idDepth;
    oDxf.slot = Double.parseDouble(txtlockslot.getText());
    oDxf.toeflap = Double.parseDouble(txttoe.getText());
    oDxf.lug = Double.parseDouble(txtlocklug.getText());
    
    oDxf.lidtabD = Double.parseDouble(txtlidtab.getText());
    oDxf.frontFlap = Double.parseDouble(txtfrontflap.getText());
    oDxf.lidTabAdd = Double.parseDouble(txttabplus.getText());
    
    oDxf.unit = "M";
    oDxf.style = "";
    oDxf.flute = (String) cflutetype.getSelectedItem();
    oDxf.cutlay = "CUT";
    oDxf.crelay = "CREASE";
    oDxf.dxf = ""; // Output File's contents
    oDxf.dxfxmax = String.valueOf(0);
    oDxf.dxfymax = String.valueOf(0);
    
    oDxf.Xaxis = 1; // Direction of X axis +1 or -1
    oDxf.Yaxis = 1; // Direction of Y axis +1 or -1
    
    outputDxfData = oDxf.Draw0421p();
    
    String outputDir = ""; // "../temp/";
    File f = new File(outputDir + txtFilePathName.getText());
    if(f.exists() && !f.isDirectory()) { 
      f.delete();
    }
    String outputDxfFile = outputDir + txtFilePathName.getText(); // "testfilel.dxf"; // "/var/www/temp/testfilel.dxf";
    
    BufferedWriter writer = null; // Note outside try block
    try {
      //create a file
      File newDxfFile = new File(outputDxfFile);
      
      writer = new BufferedWriter(new FileWriter(newDxfFile));
      writer.write(outputDxfData);
      writer.flush();
    } catch (Exception e) {
      e.printStackTrace();
    } finally {
      try {
        // Close the writer regardless of what happens...
        writer.close();
      } catch (Exception e) {
      }
    }
    JOptionPane.showMessageDialog(null, "Saved: " + outputDxfFile); 

    }//GEN-LAST:event_createDxfActionPerformed

